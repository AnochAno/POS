    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        try{
            String id = txtid.getText();
            //String city = cmbocity.getSelectedItem().toString();
            //String province = cmbopro.getSelectedItem().toString();

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pos?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "");
            Statement stm = con.createStatement();
            String sql  = "select * from sales_man_entry where sid = '"+id+"'";
            ResultSet rs = stm.executeQuery(sql);

            if(rs.next()){
                txtname.setText(rs.getString(2));
                txtadd.setText(rs.getString(3));
                cmbocity.setSelectedItem(rs.getString(4));
                cmbopro.setSelectedItem(rs.getString(5));
                txtpostal.setText(rs.getString(6));
                txtcon.setText(rs.getString(7));
                txtemail.setText(rs.getString(8));
                txtcom.setText(rs.getString(9));
                txtremark.setText(rs.getString(10));

            }else{
                JOptionPane.showMessageDialog(this, "Inavalid salesman_ID : "+id);
            }
            con.close();
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_btnsearchActionPerformed

