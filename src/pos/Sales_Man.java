    public void table(){
        try{
            connect();
            DefaultTableModel mod = (DefaultTableModel)jTable1.getModel();
            mod.setRowCount(0);
            Statement stm = con.createStatement();
            String sql = "select* from sales_man_entry";
            ResultSet rs = stm.executeQuery(sql);
            while(rs.next()){
                String d1  = rs.getString("sid");
                String d2  = rs.getString("sname");
                String d3  = rs.getString("Address");
                String d4  = rs.getString("city");
                String d5  = rs.getString("province");
                String d6  = rs.getString("postal_code");
                String d7  = rs.getString("contact");
                String d8  = rs.getString("email_id");
                String d9  = rs.getString("commission");
                String d10  = rs.getString("remark");
                
                mod.addRow(new Object[] {d1, d2, d3, d4, d5, d6, d7, d8, d9, d10});
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error while table load..."+"\n"+e);
        }
    }
    
    

