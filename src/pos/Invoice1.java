    private void txtpidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpidKeyReleased
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            String pcode = txtpid.getText();
            
            connect();
            try {
                ps = con.prepareStatement("select * from product_entry where pid = ?");
            
                ps.setString(1, pcode);
                
                ResultSet rs = ps.executeQuery();
                
                if (rs.next() == false){
                    JOptionPane.showMessageDialog(this, "Product ID not found!!!");
                }
                else{
                    String pname = rs.getString("pname");
                    String price = rs.getString("unit_price");
                    
                    lblpname.setText(pname.trim());
                    lbluprice.setText(price.trim());
                }
            } 
            catch (SQLException ex) {
                Logger.getLogger(Invoice1.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
    }//GEN-LAST:event_txtpidKeyReleased

