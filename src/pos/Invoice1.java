    public void invoice_no(){
        try{
            Statement stm = con.createStatement();
            ResultSet rs = stm.executeQuery("select MAX(invoice_no) from invoice");
            rs.next();
            
            rs.getString("MAX(invoice_no)");
            
            if(rs.getString("MAX(invoice_no)") == null){
                lblinvoice.setText("In-001");
            }
            else{
                long id = Long.parseLong(rs.getString("MAX(invoice_no)").substring(2, rs.getString("MAX(invoice_no)").length()));
                id++;
                lblinvoice.setText("In-"+String.format("%03d", id));
            }
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
        
    }
    
